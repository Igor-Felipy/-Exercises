# Quando o programa descobre a quantidade por voce!
fotos = ['foto1','foto2','foto3','foto4','foto5']
for  foto in fotos:
    print(foto)

# Voce ja sabe a quantidade de vezes que deve rodar
# Função range roda sepre uma vez a menos que o numero especificado
for volume in range(11):
    print(f'Aumentando o volume para {volume}')

# aqui voce pode escolher o primeiro e o ultimo digito a ser executado
for volume in range(1,11):
    print(f'Aumentando o volume para {volume}')

